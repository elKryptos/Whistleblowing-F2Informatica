package f2.tironcinio.whistleblowing.responses;

public class ControlDTO {
	
	private String username;
	private String email;
	private String password;
	private String timer;
	private String codiceRecupero;
	private String nome;
	private String cognome;
	private String token;
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getTimer() {
		return timer;
	}
	public void setTimer(String timer) {
		this.timer = timer;
	}
	public String getCodiceRecupero() {
		return codiceRecupero;
	}
	public void setCodiceRecupero(String codiceRecupero) {
		this.codiceRecupero = codiceRecupero;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCognome() {
		return cognome;
	}
	public void setCognome(String cognome) {
		this.cognome = cognome;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}

}
